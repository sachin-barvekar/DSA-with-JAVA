public class pr1 {
    public static void main(String[] args) {
        int a=5, b=6;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(~a);
        System.out.println(~0);
        System.out.println(a<<2);
        System.out.println(6>>1);
        System.out.println(6>>2);
        System.out.println(6>>4);
    }
}
